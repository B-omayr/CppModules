#include "ClapTrap.hpp"

int	FtAtoi(std::string s)
{
	int		res;
	int		i;

	res = 0;
	i = 0;
	while ((s[i] == '\t') || (s[i] == '\n')
		|| (s[i] == '\v') || (s[i] == '\f')
		|| (s[i] == '\r') || (s[i] == ' '))
		i++;
	if (s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res);
}